#include "3-calc.h"

/**
 * op_add - sum of a and b
 * @a: first num
 * @b: second num
 * Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of a and b
 * @a: first num
 * @b: second num
 * Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication of a and b
 * @a: first num
 * @b: second num
 * Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of a and b
 * @a: first num
 * @b: second num
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulas of a and b
 * @a: first num
 * @b: second num
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
