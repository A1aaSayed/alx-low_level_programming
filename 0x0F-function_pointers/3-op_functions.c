#include "3-calc.h"

/**
 * op_add - sum of a and b
 * @a: Num 1
 * @b: Num 2
 * Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction of a and b
 * @a: Num 1
 * @b: Num 2
 * Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplicaton of a and b
 * @a: Num 1
 * @b: Num 2
 * Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of a by b
 * @a: Num 1
 * @b: Num 2
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulas a and b
 * @a: Num 1
 * @b: Num 2
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
